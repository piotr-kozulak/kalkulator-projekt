#include <iostream>
using namespace std;
double add(double number1, double number2) {
    return number1 + number2;
}

double sub(double number1, double number2) {
    return number1 - number2;
}

double multiply(double number1, double number2) {
    return number1 * number2;
}

double div(double number1, double number2) {



    return number1 / number2;
}